Array_P pCompare(Array_P in_a, Array_P in_b)
{

	Array_P array_dif;

	for(int i=0; i<LEN_P; i++)
	{
		
		if(in_a.el[i] == in_b.el[i])
		{
			array_dif.el[i] = 1;
		} else
		{
			array_dif.el[i] = 0;
		}
			
	}

	return array_dif;

}

int pTruncatedHammingDistance(Array_P array_dif, Array_P array_P, Array_P array_element)
{

	int distance = 0;

	for(int i=0; i<LEN_P; i++)
	{
		if(array_dif.el[i] == 0)
		{
			if(array_P.el[i] != array_element.el[i])
			{	
				distance = distance + 1;
			}
		}
	}

	return distance;

}




void mpc_main(Array_P INPUT_A, Array_P INPUT_B)
{

	Array_P array_dif = pCompare(INPUT_A, INPUT_B);

	int hamm_distance_A;
	int hamm_distance_B;

	hamm_distance_A = pTruncatedHammingDistance(array_dif, pSequence, INPUT_A);
	hamm_distance_B = pTruncatedHammingDistance(array_dif, pSequence, INPUT_B);
	
	int OUTPUT_comparison;
	int comparison;

	comparison = 0;
	if(hamm_distance_A > hamm_distance_B)
	{
		comparison = 1;
	}

	
	OUTPUT_comparison = comparison;
}

